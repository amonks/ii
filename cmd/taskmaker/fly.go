package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"monks.co/pkg/config"
	"monks.co/pkg/env"
)

func generateFlyConfigs() error {
	cfg, err := config.LoadApps()
	if err != nil {
		return fmt.Errorf("loading apps config: %w", err)
	}

	flyApps := cfg.FlyApps()
	sort.Strings(flyApps)

	for _, name := range flyApps {
		app := cfg.Apps[name]
		dir := env.InMonksRoot("apps", name)

		flytoml := buildFlyToml(name, app, cfg.Defaults)
		if err := writeGenerated(filepath.Join(dir, "fly.toml"), flytoml); err != nil {
			return fmt.Errorf("writing fly.toml for %s: %w", name, err)
		}
	}

	return nil
}

func buildFlyToml(name string, app config.AppEntry, defaults config.Defaults) string {
	region := defaults.Region
	vmSize := defaults.VMSize
	if app.VMSize != "" {
		vmSize = app.VMSize
	}
	vmMemory := defaults.VMMemory
	if app.VMMemory != "" {
		vmMemory = app.VMMemory
	}

	volume := fmt.Sprintf("monks_%s_data", name)

	var b strings.Builder

	b.WriteString("# Generated by taskmaker - DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "app = %q\n", "monks-"+name)
	fmt.Fprintf(&b, "primary_region = %q\n", region)
	b.WriteString("\n")

	b.WriteString("[env]\n")
	fmt.Fprintf(&b, "  MONKS_APP_NAME = %q\n", name)
	b.WriteString("  MONKS_ROOT = \"/app\"\n")
	b.WriteString("  MONKS_DATA = \"/data\"\n")
	b.WriteString("  TSNET_FORCE_LOGIN = \"1\"\n")
	b.WriteString("\n")

	b.WriteString("[[vm]]\n")
	fmt.Fprintf(&b, "  size = %q\n", vmSize)
	fmt.Fprintf(&b, "  memory = \"%s\"\n", vmMemory)

	b.WriteString("\n")
	b.WriteString("[[mounts]]\n")
	fmt.Fprintf(&b, "  source = %q\n", volume)
	b.WriteString("  destination = \"/data\"\n")

	if app.Public {
		b.WriteString("\n")
		b.WriteString("[[services]]\n")
		b.WriteString("  internal_port = 4433\n")
		b.WriteString("  protocol = \"tcp\"\n")
		b.WriteString("  [[services.ports]]\n")
		b.WriteString("    port = 443\n")
		b.WriteString("    handlers = [\"proxy_proto\"]\n")
		b.WriteString("\n")
		b.WriteString("[[services]]\n")
		b.WriteString("  internal_port = 8080\n")
		b.WriteString("  protocol = \"tcp\"\n")
		b.WriteString("  [[services.ports]]\n")
		b.WriteString("    port = 80\n")
		b.WriteString("    handlers = [\"proxy_proto\"]\n")
		b.WriteString("\n")
		b.WriteString("[metrics]\n")
		b.WriteString("  port = 9999\n")
		b.WriteString("  path = \"/metrics\"\n")
	}

	return b.String()
}

func writeGenerated(path string, content string) error {
	return os.WriteFile(path, []byte(content), 0644)
}
