package main

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"sort"
	"strings"

	"github.com/BurntSushi/toml"
)

type module struct {
	dir        string // relative to repo root, e.g. "pkg/serve"
	modulePath string // Go module path, e.g. "monks.co/pkg/serve"
	hasGoMod   bool   // whether go.mod already exists
}

// discoverModules finds all {apps,pkg,cmd}/* directories containing .go files,
// plus any nested subdirectories that already have their own go.mod.
// Module path defaults to "monks.co/{dir}".
func discoverModules(root string) ([]module, error) {
	var mods []module
	seen := map[string]bool{}
	for _, prefix := range []string{"apps", "pkg", "cmd"} {
		prefixDir := filepath.Join(root, prefix)
		entries, err := os.ReadDir(prefixDir)
		if err != nil {
			if os.IsNotExist(err) {
				continue
			}
			return nil, fmt.Errorf("reading %s: %w", prefixDir, err)
		}
		for _, e := range entries {
			if !e.IsDir() {
				continue
			}
			dir := filepath.Join(prefix, e.Name())
			absDir := filepath.Join(root, dir)

			if !hasGoFiles(absDir) {
				continue
			}

			_, err := os.Stat(filepath.Join(absDir, "go.mod"))
			hasGoMod := err == nil

			mods = append(mods, module{
				dir:        dir,
				modulePath: "monks.co/" + dir,
				hasGoMod:   hasGoMod,
			})
			seen[dir] = true

			// Also discover nested modules (subdirectories with their own go.mod).
			filepath.WalkDir(absDir, func(path string, d os.DirEntry, err error) error {
				if err != nil {
					return err
				}
				if !d.IsDir() {
					return nil
				}
				rel, _ := filepath.Rel(root, path)
				if seen[rel] {
					return nil
				}
				if _, serr := os.Stat(filepath.Join(path, "go.mod")); serr == nil {
					seen[rel] = true
					mods = append(mods, module{
						dir:        rel,
						modulePath: "monks.co/" + rel,
						hasGoMod:   true,
					})
				}
				return nil
			})
		}
	}
	sort.Slice(mods, func(i, j int) bool {
		return mods[i].dir < mods[j].dir
	})
	return mods, nil
}

// hasGoFiles reports whether dir or any of its subdirectories contain .go files.
func hasGoFiles(dir string) bool {
	found := false
	filepath.WalkDir(dir, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if !d.IsDir() && strings.HasSuffix(d.Name(), ".go") {
			found = true
			return filepath.SkipAll
		}
		return nil
	})
	return found
}

// generateGoWork produces the contents of a go.work file.
func generateGoWork(mods []module) string {
	var b strings.Builder
	b.WriteString("// Code generated by taskmaker - DO NOT EDIT.\n\n")
	b.WriteString("go 1.26.1\n\n")
	b.WriteString("use (\n")
	b.WriteString("\t.\n")
	for _, m := range mods {
		fmt.Fprintf(&b, "\t./%s\n", m.dir)
	}
	b.WriteString(")\n")
	return b.String()
}

// generateGoMod produces a minimal go.mod for a new module.
func generateGoMod(modulePath string) string {
	var b strings.Builder
	fmt.Fprintf(&b, "module %s\n\ngo 1.26.1\n", modulePath)
	return b.String()
}

// writeModuleFiles writes go.mod for any module that doesn't already have one.
func writeModuleFiles(root string, mods []module) error {
	for _, m := range mods {
		if m.hasGoMod {
			continue
		}
		path := filepath.Join(root, m.dir, "go.mod")
		content := generateGoMod(m.modulePath)
		if err := os.WriteFile(path, []byte(content), 0644); err != nil {
			return fmt.Errorf("writing %s: %w", path, err)
		}
	}
	return nil
}

// writeGoWork writes the go.work file at the repo root.
func writeGoWork(root string, mods []module) error {
	path := filepath.Join(root, "go.work")
	content := generateGoWork(mods)
	return os.WriteFile(path, []byte(content), 0644)
}

// publishConfig represents config/publish.toml.
type publishConfig struct {
	Package []publishPackage `toml:"package"`
}

type publishPackage struct {
	Dir        string `toml:"dir"`
	ModulePath string `toml:"module_path"`
	Mirror     string `toml:"mirror"`
}

// loadPublishConfig loads config/publish.toml if it exists.
// Returns an empty config if the file doesn't exist.
func loadPublishConfig(root string) (*publishConfig, error) {
	path := filepath.Join(root, "config", "publish.toml")
	var cfg publishConfig
	_, err := os.Stat(path)
	if os.IsNotExist(err) {
		return &cfg, nil
	}
	if _, err := toml.DecodeFile(path, &cfg); err != nil {
		return nil, fmt.Errorf("parsing %s: %w", path, err)
	}
	return &cfg, nil
}

// applyModulePathOverrides updates module paths based on publish.toml.
func applyModulePathOverrides(mods []module, cfg *publishConfig) {
	overrides := map[string]string{}
	for _, pkg := range cfg.Package {
		if pkg.ModulePath != "" {
			overrides[pkg.Dir] = pkg.ModulePath
		}
	}
	for i := range mods {
		if override, ok := overrides[mods[i].dir]; ok {
			mods[i].modulePath = override
		}
	}
}

// generateModuleFiles is the top-level entry point called from taskmaker.
// It discovers modules, writes missing go.mod files, writes go.work,
// and runs go work sync.
func generateModuleFiles(root string) error {
	cfg, err := loadPublishConfig(root)
	if err != nil {
		return err
	}

	mods, err := discoverModules(root)
	if err != nil {
		return err
	}

	applyModulePathOverrides(mods, cfg)

	if err := writeModuleFiles(root, mods); err != nil {
		return err
	}

	if err := writeGoWork(root, mods); err != nil {
		return err
	}

	// Run go work sync to align dependency versions.
	cmd := exec.Command("go", "work", "sync")
	cmd.Dir = root
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		return fmt.Errorf("go work sync: %w", err)
	}

	return nil
}
